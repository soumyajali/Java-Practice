public class Loops {
    public static void main(String[] args) {
        //for loop
        for(int i = 0;i<=10;i++){
            System.out.println((i));
        }

       /*  for(int i = 0;i<=5;i++){
            System.out.println("Hello for loop");
        }*/
    
        //while loop
        int i = 0;
        while(i<=5){
            System.out.println("Hello While loop");
            i++;
        }

        //do-while loop
        int k = 0;
        do{
            System.out.println("Hello do-while loop");
            k++;
        }
        while(k<5);
    }
    
}
